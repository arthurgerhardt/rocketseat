# Inteiro

numero_inteiro = 3
print("numero_inteiro é = ", numero_inteiro)
# Real com ponto flutuante

numero_real = 3.14
print("numero_real com ponto flutuante é = ", numero_real)

# Type ()
print("Tipo da variável inteiro é = ", type(numero_inteiro))
print("Tipo da variável real é = ", type(numero_real))

# Soma
soma = 1 + 1
print("Soma de 1 + 1 é = ", soma)

# Subtração
subtracao = 1 - 1
print("Subtração de 1 - 1 é = ", subtracao)

# Multiplicação
multiplicacao = 2 * 2
print("Multiplicação de 2 x 2 é = ", multiplicacao)

# Divisão
divisao = 5 / 2
print("Divisão de 5 / 2 é = ", divisao)
print("Tipo de variável divisão é = ", type(divisao))

# int()
print("Valor em inteiro é: ", int(divisao))

# float()
print("Valor em ponto flutuante é: ", float(soma))

# Módulo
print("Resto da divisão de 5 / 2 é = ", 5 % 2)

# Divisão Inteira
print("Divisão Inteira de 5 / 2 é = ", 5 // 2)
print("Tipo de variável divisão inteira é = ", type(5 // 2))

